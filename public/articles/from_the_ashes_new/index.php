					<p>Remember that moment when you were cleaning up and reached back into the closet and found something old? At first it's simply an oddity you picked up some time ago. What is this thing? Why do I have it? Where did it come from? You inspect it and your mind shuffles through potential memories. And then you remember. You remember that you loved it. It's a relic from an activity you've forgotten about. It's a part of you that you've lost touch with. In that moment you wonder how could I have let something that was so important to me fade away?</p>

					<p>Writing is my lost object sitting in the back of a drawer or at the bottom of an unpacked box. I tried to do it before&mdash;right here, even&mdash;but it didn't stick. Then I decided that there were too many barriers here and tried it in a simpler manner elsewhere. That didn't work either. Every avenue I've tried had failed me or, more likely, I failed it.</p>

					<p>Failure is a great thing, though. I love it and you should love it, too. The aftermath of failure is the place where we sculpt who we are. Was it your fault? Yes? Be better next time. Was it your fault? No? Do what you can to keep it from happening again.</p>

					<p>I failed and it was my fault, so I'm going to try again. Why? Because I enjoy doing this writing thing. Because that's how it should be when confronted with something you enjoy; you should just be able to do it. Previously, I've told myself that I need a beautiful web site to host this writing; I'm a designer after all. I've told myself I need a better content management system; I'm a coder, too. I've told myself that no one will care enough to read what I write; I'm not a professional writer. All these excuses have kept me from writing.</p>

					<p>So, most importantly, I'm going to write. Design will exist to compliment most of the writing. It will slowly trickle up to the entire site, too. I didn't want to get hung up on design, though. Right now I'll be manually adding each article and updating the site when there's new content. Eventually I'll need some way to manage this content. I didn't want to get hung up on architecture, though.</p>

					<p>I'm removing the barriers I made so I can ultimately do something I want to do. Maybe that's all it takes--boil those things you want down to their basic parts so you can't say no to them anymore.</p>

					<p>I'm a designer, a developer, and geek. This is the place where I write.</p>
				
